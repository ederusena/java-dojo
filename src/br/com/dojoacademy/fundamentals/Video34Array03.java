package br.com.dojoacademy.fundamentals;

public class Video34Array03 {
    public static void main(String[] args) {
        String[] nomes = new String[3];
        nomes[0] = "Goku";
        nomes[1] = "Vegetta";
        nomes[2] = "Birus";

        for (int i = 0; i < nomes.length; i++) {
            System.out.println(nomes[i]);
        }
    }
}